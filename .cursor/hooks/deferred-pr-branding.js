#!/usr/bin/env node
'use strict';

// Deferred PR branding replacement for Cursor.
// Cursor appends "Made with Cursor" AFTER afterShellExecution fires,
// so this script runs detached with retry backoff to catch it.

const { execSync } = require('child_process');

const pr = process.argv[2];
const repo = process.argv[3];

if (!pr || !repo) process.exit(0);

const delays = [3000, 5000, 8000];

(async () => {
  for (const delay of delays) {
    await new Promise(r => setTimeout(r, delay));
    try {
      const body = execSync(
        `gh pr view ${pr} --repo ${repo} --json body --jq .body`,
        { encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'], timeout: 10000 }
      ).trim();

      let newBody = body;
      // Replace Cursor branding
      newBody = newBody
        .replace(/Made with \[Cursor\]\([^)]*\)/g, '⟁ Generated by [AW](https://github.com/GoHighLevel/ghl-agentic-workspace)')
        .replace(/Made with Cursor/g, '⟁ Generated by AW');
      // Replace Claude Code branding
      newBody = newBody
        .replace(/🤖\s*Generated with \[Claude Code\]\([^)]*\)/g, '⟁ Generated by [AW](https://github.com/GoHighLevel/ghl-agentic-workspace)')
        .replace(/🤖\s*Generated with Claude Code/g, '⟁ Generated by AW');

      if (newBody !== body) {
        execSync(
          `gh pr edit ${pr} --repo ${repo} --body -`,
          { input: newBody, stdio: ['pipe', 'pipe', 'pipe'], timeout: 10000 }
        );
        process.stderr.write(`[ECC] AW branding applied to PR #${pr}\n`);
        process.exit(0);
      }
    } catch (e) {
      process.stderr.write(`[ECC] AW branding retry: ${e.message?.split('\n')[0]}\n`);
    }
  }
})();
