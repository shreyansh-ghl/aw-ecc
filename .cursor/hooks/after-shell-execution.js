#!/usr/bin/env node
const { execSync } = require('child_process');
const { existsSync } = require('fs');
const { join } = require('path');
const { readStdin, hookEnabled } = require('./adapter');

readStdin().then(raw => {
  try {
    const input = JSON.parse(raw || '{}');
    const cmd = String(input.command || input.args?.command || '');
    const output = String(input.output || input.result || '');

    if (hookEnabled('post:bash:pr-created', ['standard', 'strict']) && /\bgh\s+pr\s+create\b/.test(cmd)) {
      const m = output.match(/https:\/\/github\.com\/[^/]+\/[^/]+\/pull\/\d+/);
      if (m) {
        console.error('[ECC] PR created: ' + m[0]);
        const repo = m[0].replace(/https:\/\/github\.com\/([^/]+\/[^/]+)\/pull\/\d+/, '$1');
        const pr = m[0].replace(/.+\/pull\/(\d+)/, '$1');
        console.error('[ECC] To review: gh pr review ' + pr + ' --repo ' + repo);

        // AW branding: replace Cursor/Claude branding with AW in PR body
        const cwd = input.cwd || input.workspace_roots?.[0] || process.cwd();
        const isAwLinked = existsSync(join(cwd, '.aw'));
        if (isAwLinked) {
          try {
            const body = execSync(
              `gh pr view ${pr} --repo ${repo} --json body --jq .body`,
              { encoding: 'utf8', stdio: ['pipe', 'pipe', 'pipe'], timeout: 10000 }
            ).trim();

            let newBody = body;
            // Replace Cursor branding
            newBody = newBody
              .replace(/Made with \[Cursor\]\([^)]*\)/g, '⟁ Generated by [AW](https://github.com/GoHighLevel/ghl-agentic-workspace)')
              .replace(/Made with Cursor/g, '⟁ Generated by AW');
            // Replace Claude Code branding
            newBody = newBody
              .replace(/🤖\s*Generated with \[Claude Code\]\([^)]*\)/g, '⟁ Generated by [AW](https://github.com/GoHighLevel/ghl-agentic-workspace)')
              .replace(/🤖\s*Generated with Claude Code/g, '⟁ Generated by AW');

            if (newBody !== body) {
              execSync(
                `gh pr edit ${pr} --repo ${repo} --body -`,
                { input: newBody, stdio: ['pipe', 'pipe', 'pipe'], timeout: 10000 }
              );
              console.error(`[ECC] AW branding applied to PR #${pr}`);
            }
          } catch (e) {
            console.error(`[ECC] AW branding failed: ${e.message?.split('\n')[0]}`);
          }
        }
      }
    }

    if (hookEnabled('post:bash:build-complete', ['standard', 'strict']) && /(npm run build|pnpm build|yarn build)/.test(cmd)) {
      console.error('[ECC] Build completed');
    }
  } catch {
    // noop
  }

  process.stdout.write(raw);
}).catch(() => process.exit(0));
